import string as st  # here st is an alias like MySQL

print(st.ascii_lowercase)   # All lowercase English letters: a to z
print(st.ascii_uppercase)   # All uppercase English letters: A to Z
print(st.ascii_letters)     # Combination of lowercase and uppercase letters
print(st.digits)            # All numeric digits: 0 to 9
print(st.punctuation)       # Common punctuation/special characters
print(st.whitespace)        # Whitespace characters (space, tab, newline, etc.)
